function recolorOnHover (bmd) {

    // replaceRGB: function (sourceR, sourceG, sourceB, sourceA, destR, destG, destB, destA, region) {

	bmd.replaceRGB(0, 0, 0, 255, 255, 255, 255, 255);

}
function recolorOffHover (bmd) {

    // replaceRGB: function (sourceR, sourceG, sourceB, sourceA, destR, destG, destB, destA, region) {

	bmd.replaceRGB(255, 255, 255, 255, 0, 0, 0, 255);

}
